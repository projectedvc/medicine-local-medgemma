def seed_database(db: Session) -> None:
    for login, password, full_name, role in DEMO_USERS:
        user = db.query(User).filter(User.login == login).one_or_none()
        if not user:
            db.add(User(login=login, full_name=full_name, role=role, hashed_password=hash_password(password)))

    for item in PATHOLOGIES:
        pathology = db.query(Pathology).filter(Pathology.slug == item["slug"]).one_or_none()
        if not pathology:
            db.add(Pathology(**item))

    db.commit()
